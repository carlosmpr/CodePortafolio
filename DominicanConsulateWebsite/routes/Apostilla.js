app.use(express.static(__dirname + "/public"));


router.get("/Apostilla", function(req , res){
   dbApostilla.find({}, function(err , legal){
        if(err){
            console.log(err);
            
        }else{
              res.render("serviceapostilla", {legal:legal});
        }
    
});

    
});





router.get("/apostilla/:id", function(req , res){
   dbApostilla.findById(req.params.id, function(err , fPasaporte){
        if(err){
            console.log(err);
            
        }else{
              res.render("progressno.ejs", {fPasaporte:fPasaporte});
        }
    
});

    
});











module.exports = router;